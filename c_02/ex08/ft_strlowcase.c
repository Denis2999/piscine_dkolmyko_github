char	*ft_strlowcase(char *str)
{
	int	counter;

	counter = 0;
	while (str[counter] != '\0')
	{
		if ('A' <= str[counter] && str[counter] <= 'Z')
		{
			str[counter] += 32;
		}
		counter++;
	}
	return (str);
}
